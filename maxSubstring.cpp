#include <string>
#include <iostream>
using namespace std;

class Solution{
    public:
        int lengthOfLongestSubstring(string &s){
            int n = s.length();
            int ans = 0;

            for (int i= 0; i < n; i++){
                continue;
            }
            return ans;
        }
};
int main(){
    Solution x;
    string s = "hello";
    x.lengthOfLongestSubstring(s);
    return 0;
}